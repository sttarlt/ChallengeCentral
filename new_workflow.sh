#!/bin/bash

# هذا الملف هو لتكوين سير العمل الجديد للتطبيق
# الذي يعمل على المنفذ 8080

echo "===== بدء تشغيل تطبيق مسابقاتي على المنفذ 8080 ====="
echo "التاريخ والوقت: $(date)"

# تعيين متغيرات البيئة
export PORT=8080

# بدء تشغيل التطبيق باستخدام gunicorn على المنفذ 8080
exec gunicorn --bind 0.0.0.0:8080 --reuse-port --workers 2 --reload main:app